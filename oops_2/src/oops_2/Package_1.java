package oops_2;

public class Package_1 {

	public static void main(String[] args) {

		int i=3;
		
		switch(i)
		{
		case 1:
		System.out.println("A");
			break;
			
		case 2: 
		System.out.println("B");
			break;
		case 3:
		System.out.println("E");
			break;
			
		default :
			System.out.println("C");
				
		}
	}

}
